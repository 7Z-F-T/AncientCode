public class DifferentTypes3{
	public static void main(String args[]){
		int intResult,intRemainder;
		double doubleResult,doubleRemainder;
		intResult=10/3;
    intRemainder=10%3;
    doubleResult=10.0/3;
    doubleRemainder=10.0%3;
    System.out.println(intResult);
    System.out.println(intRemainder);
    System.out.println(doubleResult);
    System.out.println(doubleRemainder);
  }
}